package main;

public class Conditionals {
	
	public static void main(String[] args) {
		int number = 20;
		
		
		if(number > 20) {
			
			System.out.println("The number is more 20.");
			
		}
		
		else if (number == 20) {
			
			System.out.println("The number is 20.");
		}
		
		else {
			
			System.out.println("The number is less than 20.");
		}
		
	}
	

}
